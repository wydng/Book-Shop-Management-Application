    private void suaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_suaActionPerformed
        // TODO add your handling code here:
        if(tbl.getSelectedRowCount()==1)
        {
            if(JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn sửa không?", "Thông báo", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
                String maS = ma.getText().trim();
                String tenS = tenSach.getText().trim();
                String loaiS = loai.getSelectedItem().toString();
                String tacG = tacgia.getText().trim();
                String nhaxb = nxb.getText().trim();
                String nhacc = ncc.getText().trim();
                String loaiB = bia.getText().trim();
                String hA = anh.getText().trim();
                String soL = sl.getText().trim();
                String namXb= namxb.getText().trim();
                String G=gia.getText().trim();
                this.loai.setEditable(false);
                String sql ="update Sach set tenSach = N'"+tenS+"',  \n" + 
//                "maLoai = (select left("+loaiS.trim()+",2)from loaiSach where maLoai=convert(int,LEFT(+"+loaiS.trim()+",2))),"+
                "tacGia = N'"+tacG+"',nhaXuatBan=N'"+nhaxb+"',nhaCungCap=N'"+nhacc+"',loaiBia=N'"+loaiB+"',hinhAnh='"+hA+"',soLuong='"+soL+"',namXB='"+namXb+"',Gia='"+G+"'\n" +
                "where maSach='"+maS+"'";
                
                sachdao.ThemXoaSua(sql);
                dsS = sachdao.layDanhSachS();
                laydulieuS(dsS);
                JOptionPane.showMessageDialog(this, "Sửa thành công !", "Thông báo", 1);
            }
        }else{
            if(tbl.getRowCount()==0) 
                JOptionPane.showMessageDialog(this, "Bảng trống","Thông báo",2);
            else
                JOptionPane.showMessageDialog(this, "Chưa chọn dòng cần sửa","Thông báo",2);
        }
    }//GEN-LAST:event_suaActionPerformed

