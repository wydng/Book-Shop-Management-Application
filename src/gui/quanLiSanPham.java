    private void laydulieuLS(ArrayList<loaiSachpojo> loaiS) {
        tblDataLSP.removeAllElements();
        for(loaiSachpojo ls : loaiS){
            Vector v = new Vector();
            v.add(ls.getMaLoai()); 
            v.add(ls.getTenLoai());
            tblDataLSP.add(v);
        }
        tbl_loai.setModel(new DefaultTableModel(tblDataLSP, tblTitleLSP));
    }

    private void laydulieuS(ArrayList<sachpojo> dsS) {
        tblDataSP.removeAllElements();
        for (sachpojo s:dsS)
       {
           Vector<Object> v = new Vector<Object>();
           v.add(s.getMaSach());
           v.add(s.getTenSach());
           v.add(s.getMaLoai());
           v.add(s.getTacGia());
           v.add(s.getNhaXuatBan());
           v.add(s.getNhaCungCap());
           v.add(s.getLoaiBia());
           v.add(s.getSoLuong());
           v.add(s.getNamXB());
           v.add(s.getGia());
           v.add(s.getHinhAnh());
           tblDataSP.add(v);
       }
        tbl.setModel(new DefaultTableModel(tblDataSP, tblTitleSP));
    }

    private void setcbo_loaisp() {
       for(loaiSachpojo i : loaiS){
            loai.addItem(i.getTenLoai());
        }
    }

